const cardsContainer = document.getElementById("cardsContainer");
const searchInput = document.getElementById("searchInput");
const searchMoviesBtn = document.getElementById("searchMovies");
const typeFilter = document.getElementById("typeFilter");
const sortFilter = document.getElementById("sortFilter");

function renderMovies(movies) {
  cardsContainer.innerHTML = "";
  if (movies.length === 0) {
    cardsContainer.innerHTML = `<p style="text-align: center; color: red;">No movies found</p>`;
    return;
  }
  movies.forEach((movie) => {
    const card = document.createElement("div");
    card.classList.add("card");
    card.innerHTML = `
      <img src="${movie.ImageURL}" alt="${movie.Title}">
      <div class="card-content">
        <h2>${movie.Title}</h2>
        <p>${movie.movie_year} • ${movie.runtime} min</p>
        <p>${movie.Categories}</p>
        <p>Rating: ${movie.imdb_rating}</p>
        <button onclick="window.open('https://www.imdb.com/title/${movie.imdb_id}', '_blank')">More Info</button>
      </div>
    `;
    cardsContainer.appendChild(card);
  });
}


function searchMovies() {
  const query = searchInput.value.toLowerCase().trim();
  const filteredMovies = movies.filter((movie) =>
    movie.Title.toLowerCase().includes(query)
  );
  renderMovies(filteredMovies);
}


function filterByType() {
  const selectedType = typeFilter.value;
  if (selectedType === "all") {
    renderMovies(movies);
  } else {
    const filteredMovies = movies.filter((movie) =>
      movie.Categories.toLowerCase().includes(selectedType.toLowerCase())
    );
    renderMovies(filteredMovies);
  }
}


function sortMovies() {
  const sortOption = sortFilter.value;
  let sortedMovies = [...movies];

  if (sortOption === "title-asc") sortedMovies.sort((a, b) => a.Title.localeCompare(b.Title));
  else if (sortOption === "title-desc") sortedMovies.sort((a, b) => b.Title.localeCompare(a.Title));
  else if (sortOption === "rating-asc") sortedMovies.sort((a, b) => a.imdb_rating - b.imdb_rating);
  else if (sortOption === "rating-desc") sortedMovies.sort((a, b) => b.imdb_rating - a.imdb_rating);
  else if (sortOption === "year-asc") sortedMovies.sort((a, b) => a.movie_year - b.movie_year);
  else if (sortOption === "year-desc") sortedMovies.sort((a, b) => b.movie_year - a.movie_year);

  renderMovies(sortedMovies);
}

searchMoviesBtn.addEventListener("click", searchMovies);
typeFilter.addEventListener("change", filterByType);
sortFilter.addEventListener("change", sortMovies);


renderMovies(movies);
